class Tree{
	
	static double heightInFeet;
	static double noOfTrees;
	static String treeName;
	
	static void setHeightInFeet(double heightInFeet){
		
		Tree.heightInFeet=heightInFeet;
	}
	
	static void setNoOfTrees(double noOfTrees){
		
		Tree.noOfTrees=noOfTrees;
	}
	
	static void setTreeName(String treeName){
		
		Tree.treeName=treeName;
	}
	
}